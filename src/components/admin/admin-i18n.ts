export type AdminLocale = "en" | "zh-CN";
export type AdminAuthErrorKey = Extract<AdminMessageKey, `auth.${string}`>;
export type AdminTranslator = (
  key: AdminMessageKey,
  params?: Record<string, string | number>,
) => string;

function interpolate(
  template: string,
  params?: Record<string, string | number>,
): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/gu, (match, key: string) =>
    Object.prototype.hasOwnProperty.call(params, key)
      ? String(params[key])
      : match,
  );
}

export function useAdminI18n(): {
  locale: AdminLocale;
  setLocale: (locale: AdminLocale) => void;
  t: AdminTranslator;
} {
  const { locale: appLocale, setLocale: setAppLocale } = useLocale();
  const locale: AdminLocale = appLocale === "zh-CN" ? "zh-CN" : "en";
  const setLocale = useCallback(
    (next: AdminLocale) => setAppLocale(next),
    [setAppLocale],
  );
  const dictionary = locale === "zh-CN" ? zh : en;
  const t = useCallback<AdminTranslator>(
    (key, params) => interpolate(dictionary[key], params),
    [dictionary],
  );

  return useMemo(
    () => ({ locale, setLocale, t }),
    [locale, setLocale, t],
  );
}
